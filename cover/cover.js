function song1(){
 
  var a = document.getElementById("play1");
  var img = document.getElementById("button1");

  if(a.paused){
    a.play();
    img.src = "./img/pause.png";
  }
    
  else{
    a.pause();
    img.src = "./img/play.png";
  }
    
}

